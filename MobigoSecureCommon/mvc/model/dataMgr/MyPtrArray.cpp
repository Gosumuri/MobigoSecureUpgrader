void CMyPtrArray::RemoveAt( int n )
{
	void* p = (void*)this->GetAt( n );
	if( p != NULL )
		CPtrArray::RemoveAt( n );
}

int CMyPtrArray::GetCountWidthoutNull()
{
	int nCount = 0;

	for( int i = 0; i < this->GetCount(); i++ ) {
		void *p = (void*)this->GetAt( i );
		if( p != NULL ) {
			nCount++;		
		}
	}

	return nCount;
}

void CMyPtrArray::SetAt( int nIndex, void* vp )
{
	CPtrArray::SetAtGrow( nIndex, vp );
}